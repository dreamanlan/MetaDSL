function(defineclass )
luaargs(base, fullName, typeName, class, obj_methods, is_value_type, is_inherit_csharp )
{
	local(lualist(base_class )assignwith(base ) );
	local(lualist(class_fields )assignwith(rawget(class, "__class_fields" ) ) );
	local(lualist(obj_fields )assignwith(nil ) );
	local(lualist(obj_field_values )assignwith(nil ) );
	local(lualist(interfaces )assignwith(rawget(class, "__interfaces" ) ) );
	local(lualist(method_info )assignwith(rawget(class, "__method_info" ) ) );
	local(lualist(is_sealed_class )assignwith(rawget(class, "__is_sealed_class" ) ) );
	local(lualist(is_static_class )assignwith(rawget(class, "__is_static_class" ) ) );
	local(function(__get_obj_methods )
	luaargs(fk )
	{
		return(obj_methods ) 
	} );
	local(function(__obj_GetType )
	luaargs(tb )
	{
		return(class ) 
	} ) 
	rawset(class, "__cs2lua_defined", true ) 
	rawset(class, "__cs2lua_fullname", fullName ) 
	rawset(class, "__cs2lua_typename", typeName ) 
	rawset(class, "__cs2lua_parent", base_class ) 
	rawset(class, "__is_value_type", is_value_type ) 
	rawset(class, "__interfaces", interfaces ) 
	rawset(class, "FullName", fullName ) 
	rawset(class, "Name", typeName ) 
	rawset(class, "__get_obj_methods", __get_obj_methods );
	if(not obj_methods )
	{
		lualist(obj_methods )assignwith(
		{
		} ) 
	} 
	rawset(obj_methods, "GetType", __obj_GetType );
	if(base_class )
	{
		lualist(obj_fields )assignwith(
		{
		} );
		local(lualist(curClass )assignwith(class ) );
		while(nil ~= curClass )
		{
			if(warmup(curClass ) )
			{
				local(lualist(ofs )assignwith(rawget(curClass, "__obj_fields" ) ) );
				if(ofs )
				{
					for(k, v )
					in(pairs(ofs ) )
					{
						rawset(obj_fields, k, v ) 
					} 
				} 
			}
			else
			{
				break 
			};
			lualist(curClass )assignwith(rawget(curClass, "__cs2lua_parent" ) ) 
		};
		if(rawget(base_class, "__get_obj_methods" ) )
		{
			rawset(obj_methods, "__index", base_class.__get_obj_methods () ) 
			setmetatable(obj_methods, obj_methods ) 
		} 
	}
	else
	{
		lualist(obj_fields )assignwith(rawget(class, "__obj_fields" ) ) 
	};
	local(function(obj_build )
	luaargs()
	{
		local(lualist(obj )assignwith(
		{
		} ) );
		if(base_class )
		{
			local(lualist(curClass )assignwith(class ) );
			while(nil ~= curClass )
			{
				if(warmup(curClass ) )
				{
					local(lualist(init_obj_fields )assignwith(rawget(curClass, "__init_obj_fields" ) ) );
					if(init_obj_fields )
					{
						init_obj_fields(obj ) 
					} 
				}
				else
				{
					break 
				};
				lualist(curClass )assignwith(rawget(curClass, "__cs2lua_parent" ) ) 
			} 
		}
		else
		{
			local(lualist(init_obj_fields )assignwith(rawget(class, "__init_obj_fields" ) ) );
			if(init_obj_fields )
			{
				init_obj_fields(obj ) 
			} 
		};
		return(obj ) 
	} );
	local(lualist(obj_meta )assignwith(
	{
		__class = class;
		__cs2lua_defined = true;
		__cs2lua_fullname = fullName;
		__cs2lua_typename = typeName;
		__cs2lua_parent = base_class;
		__is_value_type = is_value_type;
		__interfaces = interfaces;
		__index = obj_methods 
	} ) );
	if(is_inherit_csharp )
	{
		rawset(obj_meta, "__index", function()
		luaargs(t, k )
		{
			if(obj_fields )
			{
				local(lualist(ret )assignwith(obj_fields[k ] ) );
				if(nil ~= ret )
				{
					return(nil ) 
				} 
			};
			local(lualist(f )assignwith(obj_methods[k ] ) );
			if(f )
			{
				return(f ) 
			};
			local(lualist(baseObj )assignwith(rawget(t, "__base" ) ) );
			if(baseObj and __find_extern_class_or_obj_key(k, baseObj ) )
			{
				return(__find_extern_obj_member(k, baseObj ) ) 
			} 
			lualog("lookup meta for {0}.{1} base_class {2} baseObj {3}", t->GetType ().FullName , k, base_class and base_class.FullName , baseObj ) 
			printStack();
			return(nil ) 
		} ) 
		rawset(obj_meta, "__newindex", function()
		luaargs(t, k, v )
		{
			if(obj_fields )
			{
				local(lualist(ret )assignwith(obj_fields[k ] ) );
				if(nil ~= ret )
				{
					rawset(t, k, v );
					return() 
				} 
			};
			local(lualist(baseObj )assignwith(rawget(t, "__base" ) ) );
			if(baseObj and __find_extern_class_or_obj_key(k, baseObj ) )
			{
				lualist(baseObj[k ] )assignwith(v );
				return() 
			} 
		} ) 
	};
	if(base_class )
	{
		rawset(class, "__index", base_class ) 
		rawset(class, "__newindex", function()
		luaargs(t, k, v )
		{
			if(class_fields )
			{
				local(lualist(ret )assignwith(class_fields[k ] ) );
				if(nil ~= ret )
				{
					rawset(t, k, v );
					return() 
				} 
			};
			lualist(base_class[k ] )assignwith(v ) 
		} ) 
	} 
	rawset(class, "__call", function()
	luaargs(t )
	{
		local(lualist(obj )assignwith(obj_build() ) ) 
		setmetatable(obj, obj_meta );
		return(obj ) 
	} ) 
	setmetatable(class, class );
	if(rawget(class, "cctor" ) )
	{
		class.cctor () 
	};
	return(class ) 
};

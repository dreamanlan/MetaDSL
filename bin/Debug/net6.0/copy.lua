function(__find_extern_class_or_obj_key)
luaargs(k, obj)
{
	local(lualist(t)assignwith(getmetatable(obj)));
	repeat
	{
		local(lualist(fun)assignwith(rawget(t, k)));
		local(lualist(tp)assignwith(type(fun)));
		if(tp == "function")
		{
			return(true)
		}
		elseif(tp == "table")
		{
			local(lualist(f)assignwith(fun[1]));
			if(f)
			{
				return(true)
			}
		};
		lualist(t)assignwith(rawget(t, "__parent"))
	}
	until(t == nil);
	return(false)
};
